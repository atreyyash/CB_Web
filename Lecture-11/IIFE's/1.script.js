function add(a, b) {
    return a + b;
}

function substract(a, b) {
    return a - b;
}

function sqrt(a) {
    return Math.sqrt(a);
}

console.log(add(10, 20));
console.log(add(15, 30));
console.log(substract(50, 40));
console.log(substract(20, 5));
console.log(substract(20, 5));
console.log(substract(20, 5));
console.log(sqrt(225));
